// ── Component ──────────────────────────────────────────────────────────────────

interface Props {
  onSelect: (emoji: string) => void
}

export function EmojiPicker({ onSelect }: Props) {
  const [catIdx, setCatIdx] = useState(0)
  const cat = CATEGORIES[catIdx]

  return (
    <div className="bg-zinc-900 border border-zinc-700 rounded-lg overflow-hidden">
      {/* Category tabs */}
      <div className="flex border-b border-zinc-700 overflow-x-auto scrollbar-none">
        {CATEGORIES.map((c, i) => (
          <button
            key={i}
            onClick={() => setCatIdx(i)}
            title={c.label}
            className={`flex-none px-2 py-1.5 text-base transition-colors cursor-pointer ${
              catIdx === i ? 'bg-zinc-700' : 'hover:bg-zinc-800'
            }`}
          >
            {c.icon}
          </button>
        ))}
      </div>

      {/* Emoji grid */}
      <div className="max-h-44 overflow-y-auto p-1">
        {cat.sections.map((section, si) => (
          <div key={si}>
            {section.label && (
              <p className="text-[10px] text-zinc-500 uppercase tracking-wider px-1 pt-1.5 pb-0.5">
                {section.label}
              </p>
            )}
            <div className="grid grid-cols-8 gap-0">
              {section.emojis.map((emoji, ei) => (
                <button
                  key={`${si}-${ei}`}
                  onClick={() => onSelect(emoji)}
                  className="text-xl p-1 hover:bg-zinc-700 rounded transition-colors cursor-pointer leading-none"
                  title={emoji}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        ))}
      </div>
    </div>
  )
}
